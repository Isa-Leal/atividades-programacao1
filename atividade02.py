# Programa: Solicitar dois números e somá-los

# Função inicio()
def inicio():
    # Declarando as variáveis
    numero1 = float(input("Digite o primeiro número: "))  # Entrada do primeiro número
    numero2 = float(input("Digite o segundo número: "))  # Entrada do segundo número

    # Calculando a soma
    soma = numero1 + numero2

    # Exibindo o resultado
    print(f"A soma dos números é: {soma}")

# Chamada da função inicio()
inicio()

